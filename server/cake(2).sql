  arrival_time VARCHAR(24),
  -- 适用人群
  applicable VARCHAR(24),
  -- 销售总额
  sales_count INT,
  -- 上架时间
  add_time VARCHAR(24),
  -- 品牌id
  brand_id INT,
  -- 分类id
  sort_id INT,
  series_id VARCHAR(32)
);
